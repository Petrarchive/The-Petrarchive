            <dt>Maria Teresa De Luca, Associate Editor and Encoder<!-- 2020-present --></dt>
						<dd>Maria Teresa De Luca is a Ph.D. student in Italian at Rutgers University and adjunct lecturer in Italian at Columbia University. Her main research focuses on narrative theory and history of the novel, and their intersection with Italian linguistics and theory of language in the twentieth and twenty-first century, along with standing interests in Medieval Italian literature and language, especially Dante’s understanding of the nature and limits of language.
<br/>
She has extensive experience working on the main Italian etymological and historical dictionaries (LEI - <cite>Lessico Etimologico Italiano</cite>, DI - <cite>Deonomasticon Italicum</cite> and TLIO - <cite>Tesoro della Lingua Italiana delle Origini</cite>). Maria Teresa is also working on the reconstruction of the editorial history of manuscript Ashb. 956, one of the very few southern Italian vernacular translations of the <cite>Liber abaci</cite> by Leonardo Pisano, and a strategic linguistic and lexical source for understanding southern Italian culture of the Middle Ages. She is co-editor of the newly founded Digital Humanities project <cite>Divine Networks: An Interactive Visualization of Dante’s Comedy</cite>. She previously studied at the Universität des Saarlandes where she obtained a Ph.D. in Italian linguistics. Her dissertation, ‘La terminologia linguistica in <cite>Lingua nostra</cite> (1939-1978)’ was awarded the premio Giovanni Nencioni by the Accademia della Crusca.<br/>
Homepage: <a href="https://italian.rutgers.edu/people/phd-students/115-departmental-info/people/phd-graduate-students/398-maria-teresa-de-luca">https://italian.rutgers.edu/people/phd-students/115-departmental-info/people/phd-graduate-students/398-maria-teresa-de-luca</a>
<dd>
