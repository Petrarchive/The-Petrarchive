            <dt>H. Wayne Storey, Editor and Co-Principal Investigator</dt>
            <dd>H. Wayne Storey is Professor Emeritus of Italian and Medieval Studies at Indiana University, Bloomington. He has researched and published extensively on digital and material philology, textual editing, Medieval and Renaissance Italian and Old Occitan literatures. Among his notable publications are <em>Transcription and Visual Poetics in the Early Italian Lyric</em> (Garland Press, 1993), Rerum vulgarium fragmenta: <em>Facsimile del codice autografo Vaticano Latino 3195</em> (Antenore, 2003-2004) and <em>Petrarch and the Textual Origins of Interpretation</em> (Brill, 2007). Storey is President Society for Textual Scholarship
						<br/>
						Homepage: <a href="https://frit.indiana.edu/about/emeriti-faculty/storey-h.html">https://frit.indiana.edu/about/emeriti-faculty/storey-h.html</a></dd>
            <dt>John A. Walsh, Editor and Co-Principal Investigator</dt>
<dd>
John A. Walsh is the Director of the <a href="https://www.hathitrust.org/htrc">HathiTrust Research Center</a> and Associate Professor of <a href="http://ils.indiana.edu">Information and Library Science</a> in the <a href="http://sice.indiana.edu">Luddy School of Informatics, Computing, and Engineering</a> at Indiana University. His research applies computational methods to the study of literary and historical documents. Walsh is an editor of digital scholarly editions, including: the <a href="http://petrarchive.org">Petrarchive</a>, the <a href="http://swinburneproject.org">Algernon Charles Swinburne Project</a>, and the <a href="http://chymistry.org">Chymistry of Isaac Newton</a>. He developed <a href="http://cbml.org">Comic Book Markup Language</a> (CBML) for scholarly encoding of comics and graphic novels, and <a href="http://teiboilerplate.org">TEI Boilerplate</a>, for publishing documents encoded according to the Text Encoding Initiative (TEI) <a href="http://www.tei-c.org/release/doc/tei-p5-doc/en/html/index.html">Guidelines for Electronic Text Encoding and Interchange</a>. He is the founding and current Technical Editor of <a href="http://digitalhumanities.org/dhq">Digital Humanities Quarterly</a>, an open-access online journal published by the <a href="http://adho.org">Alliance of Digital Humanities Organizations</a>. Walsh’s research interests include: computational literary studies; textual studies and bibliography; text technologies; book history; 19th-century British literature, poetry and poetics; and comic books. <br />Homepage: <a href="http://johnwalsh.name">http://johnwalsh.name</a>.
            </dd>
            </dl>
            <dl>
            <dt>Isabella Magni, Editor and Co-Principal Investigator</dt>
            <dd>Isabella Magni is Lecturer in Digital Humanities at the University of Sheffield (Sheffield, UK). In addition to her work as Editor of the Petrarchive, Isabella is also Co-Investigator of the <a href="https://wiki.htrc.illinois.edu/pages/viewpage.action?pageId=87785594">SCWAReD</a> project in collaboration with the HathiTrust Research Center, and Editor of <a href="https://italian-paleography.library.utoronto.ca">Italian Paleography</a> (with Lia Markey and Maddalena Signorini). She teaches and conducts research in digital humanities and digital philology, Medieval and Early modern literature, history of the book, Petrarch and Dante. She co-edited a volume on <a href="https://brill.com/view/title/58363">Interpretation and visual poetics in Medieval and Early Modern texts</a> (2021), edited a special issue on <a href="https://emdr.itercommunity.org/index.php/emdr/issue/view/8">Digital Paleography</a> for the Early Modern Digital Review journal (2020) and serves as digital editor for <a href="http://textual-cultures.org/">Textual Cultures</a> journal. She is currently working on a new digital edition of the <a href="http://isamagni.com/?page_id=228#albizzi ">Albizi Memorial Book</a> and a monograph on forms and textualities of Medieval memorial books.<br/>
						Before joining the University of Sheffield, Isabella was Postdoctoral Fellow at HTRC and Indiana University (2021-2022); Postdoctoral Associate in Italian and Digital Humanities at Rutgers University (2019-2021); and Mellon Postdoctoral Fellow in Digital Paleography at the Newberry Library in Chicago (2017-2019). She earned her PhD in Italian and Medieval Studies at Indiana University (2017).
            <br/>
            Homepage: <a href="https://www.sheffield.ac.uk/dhi/about-dhi/people/isabella-magni">https://www.sheffield.ac.uk/dhi/about-dhi/people/isabella-magni</a>.<br/>
            </dd>
