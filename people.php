						<dd>Paolo Scartoni is a Ph.D. student at Rutgers University. His research focuses on the relationship between musica speculativa and grammar in Dante’s linguistics and poetics. In addition to collaborate as an encoder at Petr<em>archive</em>, he is also working as a contributor at the Deiphira Project, the digital edition of a witness of Alberti's Deiphira (Harvard University's Houghton Library, MS Typ. 422). Prior to joining Rutgers, he earned a M.M. from the Conservatory of Perugia (Italy) where he graduated in Historical Piano and defended a thesis on the role of literature in Robert Schumann’s music for piano.
